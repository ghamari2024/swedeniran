_INDUSTRY_EN_TO_SV = {v.lower(): k for k, v in INDUSTRY_TRANSLATIONS.items()}


def translate_role(value: str | None) -> str | None:
    if not value:
        return value
    return ROLE_TRANSLATIONS.get(value, value)


def translate_industry(value: str | None) -> str | None:
    if not value:
        return value
    return INDUSTRY_TRANSLATIONS.get(value, value)


def translate_industries(values: list[str]) -> list[str]:
    return [translate_industry(v) or v for v in values]


def industry_filter_value(label_or_sv: str) -> str | None:
    """Resolve an English filter label back to the stored Swedish value."""
    if not label_or_sv:
        return None
    if label_or_sv in INDUSTRY_TRANSLATIONS:
        return label_or_sv
    return _INDUSTRY_EN_TO_SV.get(label_or_sv.lower())


